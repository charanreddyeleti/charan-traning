function grade(a)
			{
              if (a >= 0 && a <= 10 )
				return " grade = E";
			else if (a >= 11 && a <= 20 )
				return " grade = d";
			else if (a >= 21 && a <= 30 )
				return " grade = c";
			else if (a >= 31 && a <= 40 )
				return " grade = b";
			else if (a >= 41 && a <= 50 )
				return " grade = a";              
			}
			